const COUNTRY_LANGUAGE_MAP: Record<string, string> = {
  TR: 'tr',
  US: 'en', GB: 'en', AU: 'en', CA: 'en', NZ: 'en', IE: 'en', ZA: 'en',
  DE: 'de', AT: 'de', CH: 'de',
  AE: 'ar', SA: 'ar', EG: 'ar', KW: 'ar', QA: 'ar', BH: 'ar', OM: 'ar', JO: 'ar', LB: 'ar', IQ: 'ar',
  NL: 'en', FR: 'en', ES: 'en', IT: 'en', PT: 'en', SE: 'en', NO: 'en', DK: 'en', FI: 'en', PL: 'en',
}

const SUPPORTED_LANGS = ['tr', 'en', 'de', 'ar']

export async function initI18n(country: string) {
  if (i18n.isInitialized) return

  // Kullanıcının kayıtlı dil tercihini kontrol et
  let savedLang: string | null = null
  try {
    savedLang = await AsyncStorage.getItem('app_language')
  } catch {}

  const lng = (savedLang && SUPPORTED_LANGS.includes(savedLang))
    ? savedLang
    : (COUNTRY_LANGUAGE_MAP[country] ?? 'en')

  await i18n.use(initReactI18next).init({
    resources: {
      tr: { translation: tr },
      en: { translation: en },
      de: { translation: de },
      ar: { translation: ar },
    },
    lng,
    fallbackLng: 'en',
    interpolation: { escapeValue: false },
  })
}

export async function changeLanguage(lang: string) {
  await AsyncStorage.setItem('app_language', lang)
  return i18n.changeLanguage(lang)
}

export function getCurrentLanguage() {
  return i18n.language
}

export function isRTL() {
  return i18n.language === 'ar'
}

